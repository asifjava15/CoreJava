public class AssignmentEx {
	/**
	 * We are performing assignment operators here
	 * 
	 * @param args
	 */
	public static void main(String[] args) {
		int x=10;
		x+=10;
		System.out.println(x);
		x-=10;
		System.out.println(x);
		x*=10;
		System.out.println(x);
		x/=10;
		System.out.println(x);
		x%=10;
		System.out.println(x);
	}
}
